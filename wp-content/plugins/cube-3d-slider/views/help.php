<h2>Cube 3D slider help</h2>

<h3>Shortcode parameters</h3>

<ul>
	<li>arrows : if set to false, don't display next and prev arrows (default value is true)</li>
	<li>autoplay : if set to true, the cube slider will turn automatically (default value is false)</li>
</ul>

<p>Example : [cube-3d-slider id=1 arrows=false autoplay=true]</p>